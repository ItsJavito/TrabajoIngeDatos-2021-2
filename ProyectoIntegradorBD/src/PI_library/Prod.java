    private void getProducts() throws SQLException{
        Statement stm = reg.createStatement();
        String sql = "SELECT * FROM PRODUCTO ORDER BY SKU";
        ResultSet rs = stm.executeQuery(sql);
        String col[] = {"SKU" , "PRECIO" , "NOMBRE" , "PESO" , "MARCA" , "SUBCATEGORIA"};
        DefaultTableModel tableModel = new DefaultTableModel(col, 0);
        while(rs.next()){
            String sku = rs.getString("SKU");
            String precio = rs.getString("PRECIO");
            String name = rs.getString("NOMBRE");
            String peso = rs.getString("PESO");
            String mnam = rs.getString("MNAME");
            String subc = rs.getString("SUBC");
            String row[] = {sku , precio, name, peso, mnam , subc};
            tableModel.addRow(row);
        }
        jTable1.setModel(tableModel);
    }
    
    private void eliminarProducto() throws SQLException{
        try{
            int row = jTable1.getSelectedRow();
            if(row >= 0){
                Statement stm = reg.createStatement();
                String sku = String.valueOf(jTable1.getModel().getValueAt(row, 0));
                String sql = "DELETE FROM PRODUCTO WHERE SKU = " + sku; 
                stm.executeUpdate(sql);
                javax.swing.JOptionPane.showMessageDialog(this, "Se ha borrado correctamente el producto. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el producto a borrar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
        }catch (HeadlessException | SQLException ex) {
            Logger.getLogger(Prod.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
