    private void updtProd() throws SQLException{
        try{
            if(edition){
                if(lbl_sku.getText().equals("") || lbl_peso.getText().equals("")
                        || lbl_precio.getText().equals("") || lbl_name.getText().equals("")
                        || box_marca.getSelectedItem().equals("--------MARCA--------") 
                        || box_subc.getSelectedItem().equals("--------SUBCATEGORIA--------"))
                {
                    javax.swing.JOptionPane.showMessageDialog(this, "Algunos campos faltan completar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                
                Statement st = reg.createStatement();
                int sku = Integer.parseInt(lbl_sku.getText());
                float peso = Float.parseFloat(lbl_peso.getText());
                float precio = Float.parseFloat(lbl_precio.getText());
                String name = lbl_name.getText();
                String marca = String.valueOf(box_marca.getSelectedItem());
                String subc = String.valueOf(box_subc.getSelectedItem());
                
                String sql = "UPDATE PRODUCTO SET "
                        + "PESO ="+ peso +" , "
                        + "NOMBRE = '"+name+"' , "
                        + "PRECIO = " + precio +", "
                        + "MNAME = '"+marca+"', "
                        + "SUBC = '"+subc+ "' "
                        + "where SKU = " + sku;
                st.executeUpdate(sql);
                javax.swing.JOptionPane.showMessageDialog(this, "Se ha actualizado con √©xito. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                if(lbl_sku.getText().equals("") || lbl_peso.getText().equals("")
                        || lbl_precio.getText().equals("") || lbl_name.getText().equals("")
                        || box_marca.getSelectedItem().equals("--------MARCA--------") 
                        || box_subc.getSelectedItem().equals("--------SUBCATEGORIA--------"))
                {
                    javax.swing.JOptionPane.showMessageDialog(this, "Algunos campos faltan completar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                
                Statement st = reg.createStatement();
                int sku = Integer.parseInt(lbl_sku.getText());
                float peso = Float.parseFloat(lbl_peso.getText());
                float precio = Float.parseFloat(lbl_precio.getText());
                String name = lbl_name.getText();
                String marca = String.valueOf(box_marca.getSelectedItem());
                String subc = String.valueOf(box_subc.getSelectedItem());
                String sql = 
                        "INSERT INTO PRODUCTO VALUES"
                        + "("+ precio + ", "+ sku + ", '"+name+"' , "+peso+", '"+ marca +"' , '"+subc+"')";
                st.executeUpdate(sql);
                javax.swing.JOptionPane.showMessageDialog(this, "SE HA INSERTADO EL PRODUCTO CON EXITO. \n", "COMPLETADO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            }   
        }catch (NumberFormatException | SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "HA OCURRIDO UN ERROR. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void getBoxes() throws SQLException{
        Statement st = reg.createStatement();
        
        // box subcategoria
        String sql = "SELECT * FROM SUBCAT";
        ResultSet rs = st.executeQuery(sql);
        DefaultComboBoxModel boxModel = new DefaultComboBoxModel(new String [] {"--------SUBCATEGORIA--------"});
        while(rs.next()){
            boxModel.addElement(rs.getString("SUBC"));
        }
        box_subc.setModel(boxModel);
        
        //box marca
        sql = "SELECT * FROM MARCA";
        rs = st.executeQuery(sql);
        boxModel = new DefaultComboBoxModel(new String [] {"--------MARCA--------"});
        while(rs.next()){
            boxModel.addElement(rs.getString("MNAME"));
        }
        box_marca.setModel(boxModel);
    }
    
